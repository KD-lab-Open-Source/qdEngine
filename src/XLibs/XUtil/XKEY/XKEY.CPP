int xtGetKeyName(int vkey,int shift,int russian)
{
	int offs = 0;

	if(shift) offs += 256;
	if(russian) offs += 512;

	return XKeyConvTable[offs + vkey];
}

int xtGetKeyState(int vk)
{
	return (GetKeyState(vk) & 0x01);
}
